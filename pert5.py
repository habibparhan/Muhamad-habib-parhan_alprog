#operasi aritmatika

a = 15 #digit nim pertama
b = 5 #digit nim terakhir

#operasi penjumlahan + 
hasil = a + b
print("hasil dari a + b = ", hasil)

#operasi pengurangan - 
hasil = a - b
print("hasil dari a - b = ", hasil)

#operasi perkalian *
hasil = a * b
print("hasil dari a * b = ", hasil)

#operasi pembagian / 
hasil = a / b 
print("hasil dari a / b = ", hasil)
 
 #operasi pangkat **
hasil = a ** b
print("hasil dari a ** b = ", hasil)

#operasi modus % 
hasil = a % b
print("hasil dari a % b = ", hasil)

#operasi floor division //
hasil = a // b
print("hasil dari a // b = ", hasil)