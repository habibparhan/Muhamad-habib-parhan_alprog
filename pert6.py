# program konversi suhu 

celcius = float(input(("masukan suhu anda :")))
print("suhu anda dalam celcius :", celcius)

#fahrenheit 9/5 x celcius + 32
fahrenheit = (9/5 * celcius) + 32
print("suhu anda dalam fahrenheit :",fahrenheit)
